CREATE TABLE products (
id UUID NOT NULL PRIMARY KEY,
id_auto INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
name VARCHAR(128) NOT NULL,
site_name VARCHAR(48) NOT NULL,
site_link VARCHAR(256) NOT NULL,
old_price VARCHAR(16) NOT NULL,
new_price VARCHAR(16) NOT NULL,
remaining_quantity VARCHAR(12) NOT NULL,
limit_quantity VARCHAR(12) NOT NULL,
img_string VARCHAR(128) NOT NULL,
category VARCHAR(64)
);

CREATE TABLE pages (
id UUID NOT NULL PRIMARY KEY,
id_auto INT NOT NULL GENERATED BY DEFAULT AS IDENTITY,
url VARCHAR(256) NOT NULL,
divClassName VARCHAR(64) NOT NULL,
scraperClassPath VARCHAR(128) NOT NULL,
refreshTime VARCHAR(12) NOT NULL,
hoursBetweenGet  VARCHAR(12) NOT NULL,
hoursBetweenUpdate  VARCHAR(12) NOT NULL
);



